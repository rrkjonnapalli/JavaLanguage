
class OperatorsDemo {
	public static void main(String args[]){
		int a=5,b=10;
		System.out.println("A+B is:"+(a+b));
		System.out.println("A-B is:"+(a-b));
		System.out.println("A*B is:"+(a*b));
		System.out.println("A/B is:"+(a/b));
		System.out.println("A%B is:"+(a%b));
		System.out.println("A=B is:"+(a=b));
		System.out.println("A is:"+a+" B is:"+b);
		System.out.println("A++ is:"+a++);
		System.out.println("A is:"+a+" B is:"+b);
		System.out.println("++B is:"+(++b));
		System.out.println("A is:"+a+" B is:"+b);
		System.out.println("--A is:"+(--a));
		System.out.println("A is:"+a+" B is:"+b);
		System.out.println("B-- is:"+b--);
		System.out.println("A is:"+a+" B is:"+b);
		System.out.println("~A is:"+(~a));
		System.out.println("A&B is:"+(a&b));
		System.out.println("A|B is:"+(a|b));
		System.out.println("A^B is:"+(a^b));
		a=0xFFFFFFF;
		System.out.println(a);
		System.out.println("A>>B is:"+(a>>b));
		System.out.println("A<<B is:"+(a<<b));
		
	}
}
