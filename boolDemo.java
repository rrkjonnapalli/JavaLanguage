
class boolDemo {
	public static void main(String args[]){
		boolean b;
		b = false;
		System.out.println(b);
		b = true;
		System.out.println(b);
		if(b)
			System.out.println("True Condition");
		b=false;
		if(b)
			System.out.println("True Condition");
		else
			System.out.println("False Condition");
	}
}
